from ml_final.utils.config import resolve_project_path


class ManifestImageDataset(Dataset):
    """Dataset backed by an audit CSV manifest."""

    def __init__(
        self,
        rows: list[dict[str, str]],
        *,
        label_to_idx: dict[str, int],
        transform,
    ) -> None:
        self.rows = rows
        self.label_to_idx = label_to_idx
        self.transform = transform

    def __len__(self) -> int:
        return len(self.rows)

    def __getitem__(self, index: int):
        row = self.rows[index]
        image_path = resolve_project_path(row["abs_path"])
        if image_path is None:
            raise ValueError("manifest row path cannot be empty")
        image = Image.open(image_path).convert("RGB")
        if self.transform is not None:
            image = self.transform(image)
        label = self.label_to_idx[row["label"]]
        return image, label, row["filename"]


class MultiTransformManifestDataset(Dataset):
    """Labeled manifest dataset that returns one tensor per model branch."""

    def __init__(
        self,
        rows: list[dict[str, str]],
        *,
        label_to_idx: dict[str, int],
        transforms_by_key: dict[str, Any],
    ) -> None:
        self.rows = rows
        self.label_to_idx = label_to_idx
        self.transforms_by_key = dict(transforms_by_key)

    def __len__(self) -> int:
        return len(self.rows)

    def __getitem__(self, index: int):
        row = self.rows[index]
        image_path = resolve_project_path(row["abs_path"])
        if image_path is None:
            raise ValueError("manifest row path cannot be empty")
        image = Image.open(image_path).convert("RGB")
        images = {
            key: transform(image.copy()) if transform is not None else image.copy()
            for key, transform in self.transforms_by_key.items()
        }
        label = self.label_to_idx[row["label"]]
        return images, label, row["filename"]


